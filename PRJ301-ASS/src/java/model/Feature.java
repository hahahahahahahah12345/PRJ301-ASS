package model;

/**
 *
 * @author Ha
 */
public class Feature {
    private int fid;
    private String url;

    public Feature() {}

    public Feature(int fid, String url) {
        this.fid = fid;
        this.url = url;
    }

    public int getFid() {
        return fid;
    }

    public void setFid(int fid) {
        this.fid = fid;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}

